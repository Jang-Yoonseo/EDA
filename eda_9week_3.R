#상관 계수에 대해 서술
상관계수란 두 데이터 x와 y에 대해 선형관계의 정도를 나타내는 값이다.
최대값은 1, 최솟값은 -1이며 양수일 땐 양의 상관관계 음수일 땐 음의 상관관계이다.
상관계수의 절대값이 1에 가까울 수록 직선에 가까운 관계를 가진다.

#자기 상관계수에 대해 서술
자기 상관계수는 상관계수와 달리 동일한 변수(데이터)에 대해
서로 다른 시점간의 관계를 나타내는 것이다.
자기상관계수의 시점에 따른 값은 여러번 관측을 하였을 때
어떤 데이터와 일정 차이만큼의 데이터 값이 비슷한지 혹은 다른지에 대해 알려주는 값이다.
자기상관계수의 최대값과 최소값은 각각 1,-1이다.
따라서 자기상관계수의 절대값이 1이라면 완벽히 일치하거나 완벽히 상이한 것이고
절대값이 작아질 수록 두 관측값이 무관해지는 것이다.

