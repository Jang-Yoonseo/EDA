기본적으로 신호가 주기함수인 sin(2x)함수이기 때문에 자기상관계수의 부호가 반복적으로 나타나지만 
표준정규분포에서 추출한 잡음의 크기가 신호에 미치는 영향이 꽤 크기 때문에 완벽하게 부호가 번갈아가며 나타나진 않는다.
부가설명: sin(2x)의 함수값 범위는 -1부터 1까지이다.
하지만 정규분포에서 생성한 난수는
크기가(절대값) 1보다 클 확률이 약 30%이다.
약 30퍼센트의 확률로 신호의 크기만한 잡음이 추가되니 시계열 자료는 무작위 적인 변동이 심할 것이다.
